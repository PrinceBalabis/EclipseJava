package laboration11;

public class Package {
	
	int length, width, depth;
	double weight;
	
	public int setLength(int length){
		this.length = length;
		return length;
	}
	
	public int setWidth(int width){
		this.width = width;
		return width;
	}
	
	public int setDepth(int depth){
		this.depth = depth;
		return depth;
	}
	
	public double setWeight(double weight){
		this.weight = weight;
		return weight;
	}
	
}
